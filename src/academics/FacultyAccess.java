     public static void DisplayFacultyDetails() throws SQLException {
        String username = "";
        String facultyId = "";
        String name = "";
        String phoneno = "";
        String branch = "";
        Connection con = null;
        try {
            con = DriverManager.getConnection(Login.urldatabase, Login.useroot, Login.passdatabase);
            String query = "select * from faculty where username='" + Login.loginuser + "'and password='" + Login.loginpassword + "'";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                username = rs.getString(1);
                phoneno = rs.getString(2);
                name = rs.getString(3);
                branch = rs.getString(4);
                facultyId = rs.getString(5);
            }
            String username1 = Infodetails.capitalizeString(username);
            jTextField1.setText(username1);
            jTextField2.setText(facultyId.toUpperCase());
            jTextField3.setText(phoneno);
            jTextField4.setText(username);
            jTextField5.setText(branch.toUpperCase());
            rs.close();
            st.close();
            con.close();
        } catch (SQLException e) {

        }

    }

