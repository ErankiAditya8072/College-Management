    public static boolean validorNot(int day,int month,int year)
    {
        boolean b;
        if((year % 4 == 0) || (month == 2) )
        {
            if((year % 4 == 0) && (day <=29))
            {
                b=true;
            }
            else if( ((year % 4) != 0) && (day<=28))
            {
                b=true;
            }
            else
            {
                b=false;
            }
        }
        else
        {
            if((month == 1 || month ==3 || month ==5 || month == 7 || month == 8 || month ==10 || month==12)&& ( day<=31) )
            {
                b=true;
            }
            else if((month == 2 || month == 4 || month == 6 || month ==9 || month == 11) && (day<=30))
            {
                b=true;
            }
            else
            {
                b=false;
            }
        }
        return b;
    }
  
    

