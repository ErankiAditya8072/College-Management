    public static void BooksInfoTable() {
        model.setRowCount(0);
        String book_id = "", name = "", publisher = "", edition = "", price = "";
        Connection con = null;
        String query1 = "";
        try {
            query1 = "select * from books";
            con = DriverManager.getConnection(urldatabase, useroot, passdatabase);
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            while (rs.next()) {
                book_id = rs.getString("book_id");
                name = rs.getString("name");
                publisher = rs.getString("publisher");
                edition = rs.getString("edition");
                price = rs.getString("price");
                model.addRow(new Object[]{book_id, name, publisher, edition, price});
            }

        } catch (SQLException ex) {
            Logger.getLogger(LibraryManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

